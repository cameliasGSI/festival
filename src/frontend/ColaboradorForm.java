    public void preencheColaborador() {
        nomeColaboradorInput.setText(colaborador.getNome());
        telefoneColaboradorInput.setText("" + colaborador.getTelefone());
        moradaColaboradorInput.setText(colaborador.getMorada());
        funcaoColaboradorInput.setText(colaborador.getFuncao());
        
        colaboradorFestivalCombo.setSelectedItem(festival);
        colaboradorFestivalCombo.setEditable(false);
        
        if(colaborador instanceof ColaboradorPago) {
            tipoColaboradorInput.setSelectedIndex(0);
            nifColaboradorInput.setText("" + ((ColaboradorPago) colaborador).getNif());
            pagamentoColaboradorInput.setText("" + ((ColaboradorPago) colaborador).getPagamento());
        }
        else {
            tipoColaboradorInput.setSelectedIndex(1);
        }
        
    }
    
    public void voltar(){
        JFrame janela;
        
        if (this.anterior != null) {
            janela = anterior;
        }
        else {
            janela = new Menu();
            janela.setLocationRelativeTo(null);
        }
        
        this.setVisible(false);
        janela.setVisible(true);
    }
   
