    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Colaborador colaborador;
        
        //estamos a atualizar ou criar?
        if (this.colaborador != null) {
            colaborador = this.colaborador;
        }
        else {
            if (tipoColaboradorInput.getSelectedIndex() == 0) {
                colaborador = new ColaboradorPago();
            }
            else {
                colaborador = new ColaboradorVoluntario();
            }
        }
        
        int telefoneColaborador = 0;
        
        try {
            telefoneColaborador = Integer.parseInt(telefoneColaboradorInput.getText());
        }
        catch (NumberFormatException ne) {
            System.out.println("Não foi possivel ler o numero");
        }
        
        colaborador.setNome(nomeColaboradorInput.getText());
        colaborador.setFuncao(funcaoColaboradorInput.getText());
        colaborador.setMorada(moradaColaboradorInput.getText());
        colaborador.setTelefone(telefoneColaborador);
        
        if (tipoColaboradorInput.getSelectedIndex() == 0) {
            int nifColaborador = 0;
            double pagamentoColaborador = 0;
        
            try {
                nifColaborador = Integer.parseInt(nifColaboradorInput.getText());
                pagamentoColaborador = Double.parseDouble(pagamentoColaboradorInput.getText());
            }
            catch (NumberFormatException ne) {
                System.out.println("Não foi possivel ler o numero");
            }
        
            ((ColaboradorPago)colaborador).setNif(nifColaborador);
            ((ColaboradorPago)colaborador).setPagamento(pagamentoColaborador);
        }
        
        //Se é atualização não adiciona ao arraylist porque ja la está
        if (this.colaborador == null) {
            
            Festival f = (Festival) colaboradorFestivalCombo.getSelectedItem();
            f.getColaboradores().add(colaborador);
        }
        
        anterior.refreshListaColaboradores();
        
        voltar();
    }//GEN-LAST:event_jButton2ActionPerformed

